// primitive data types

// String
let n = "ahmed";
var fname = "noman";
const lname = "rauf";

console.log(n);
console.log(fname);
console.log(lname);
console.log(typeof(n));

// Number
let age = 30;
var num = 20;
const number = 25;
console.log(age, num, number, typeof(age))

// Bigint 
 let big= 7678092798278257678298n;
 console.log(big, typeof(big))
 

// Boolean
let isStudent = true;
var isAnimal = false;
console.log(isStudent)
console.log(isAnimal)
console.log(typeof(isAnimal))


// Null
let car = null;
console.log(car)
console.log(typeof(car))

// Undefined
let address;
console.log(address)
console.log(typeof(address))

// Symbol
const sym = Symbol();
console.log(sym, typeof(sym))


// non Primitive data types:-
// Array
let fruits = ["Apple", "Banana", "Orange"];
const nums= [1,2,3,4,5];
console.log(fruits)
console.log(nums)

// Object
let person = {
  firstName: "nomi",
  lastName: "noman",
  age: 25,
  isStudent: false,
};

console.log(person, typeof(person));


